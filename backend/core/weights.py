def weights_for(position: str) -> dict[str, float]:
    """Вектор весов всех атрибутов для заданной позиции."""
    return {code: WEIGHT_MATRIX[code][position] for code in WEIGHTED_ATTRIBUTES}


def column_sum(position: str) -> float:
    return sum(WEIGHT_MATRIX[code][position] for code in WEIGHTED_ATTRIBUTES)



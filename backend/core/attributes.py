def category_of(code: str) -> str:
    return ATTRIBUTES[code][0]


def name_of(code: str) -> str:
    return ATTRIBUTES[code][1]


def attributes_by_category() -> dict[str, list[str]]:
    grouped: dict[str, list[str]] = {c: [] for c in CATEGORIES}
    for code, (cat, _, _) in ATTRIBUTES.items():
        grouped[cat].append(code)
    return grouped


def input_schema() -> list[dict]:
    """Схема формы ручного ввода: 21 атрибут, сгруппированный по шести категориям.

    Для каждого атрибута — измеряемая величина, единицы и тип поля (число или
    экспертный уровень). Используется фронтендом для построения формы (раздел 2.5.3).
    """
    schema = []
    for cat in CATEGORIES:
        items = []
        for code in WEIGHTED_ATTRIBUTES:
            if category_of(code) == cat:
                items.append({"code": code, "name": name_of(code), **ATTRIBUTE_INPUTS[code]})
        schema.append({"category": cat, "attributes": items})
    return schema
